# ES7210 Register definitions (ADC - microphone input)
ES7210_RESET_REG00 = 0x00  # Reset control
ES7210_CLOCK_OFF_REG01 = 0x01  # Used to turn off the ADC clock
ES7210_MAINCLK_REG02 = 0x02  # Set ADC clock frequency division
ES7210_MASTER_CLK_REG03 = 0x03  # MCLK source & SCLK division
ES7210_LRCK_DIVH_REG04 = 0x04  # lrck_divh
ES7210_LRCK_DIVL_REG05 = 0x05  # lrck_divl
ES7210_POWER_DOWN_REG06 = 0x06  # power down
ES7210_OSR_REG07 = 0x07
ES7210_MODE_CONFIG_REG08 = 0x08  # Set master/slave & channels
ES7210_TIME_CONTROL0_REG09 = 0x09  # Set Chip initial state period
ES7210_TIME_CONTROL1_REG0A = 0x0A  # Set Power up state period
ES7210_SDP_INTERFACE1_REG11 = 0x11  # Set sample & fmt
ES7210_SDP_INTERFACE2_REG12 = 0x12  # Pins state
ES7210_ADC_AUTOMUTE_REG13 = 0x13  # Set mute
ES7210_ADC34_MUTERANGE_REG14 = 0x14  # Set mute range
ES7210_ADC34_HPF2_REG20 = 0x20  # HPF
ES7210_ADC34_HPF1_REG21 = 0x21
ES7210_ADC12_HPF1_REG22 = 0x22
ES7210_ADC12_HPF2_REG23 = 0x23
ES7210_ANALOG_REG40 = 0x40  # ANALOG Power
ES7210_MIC12_BIAS_REG41 = 0x41
ES7210_MIC34_BIAS_REG42 = 0x42
ES7210_MIC1_GAIN_REG43 = 0x43
ES7210_MIC2_GAIN_REG44 = 0x44
ES7210_MIC3_GAIN_REG45 = 0x45
ES7210_MIC4_GAIN_REG46 = 0x46
ES7210_MIC1_POWER_REG47 = 0x47
ES7210_MIC2_POWER_REG48 = 0x48
ES7210_MIC3_POWER_REG49 = 0x49
ES7210_MIC4_POWER_REG4A = 0x4A
ES7210_MIC12_POWER_REG4B = 0x4B  # MICBias & ADC & PGA Power
ES7210_MIC34_POWER_REG4C = 0x4C

# I2C address options (AD1_AD0 pins)
ES7210_ADDR_00 = 0x80
ES7210_ADDR_01 = 0x82
ES7210_ADDR_10 = 0x84
ES7210_ADDR_11 = 0x86
